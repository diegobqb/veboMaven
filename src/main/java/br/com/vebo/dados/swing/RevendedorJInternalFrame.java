    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o revendedor selecionado?","Confirmação", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            int linhaSelecionada = revendedoresJTable.getSelectedRow();
            try {
                Revendedor revendedor = new RevendedorBO().retrieve((Long) revendedoresJTable.getValueAt(linhaSelecionada, 0));
                HibernateUtil.beginTransaction();
                new RevendedorBO().delete(revendedor);
                HibernateUtil.commitTransaction();
                JOptionPane.showMessageDialog(this, "Revendedor excluído com sucesso", "Mensagem",  JOptionPane.INFORMATION_MESSAGE);
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
            }
            atualizaTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void revendedoresJTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_revendedoresJTableMouseClicked
        if (evt.getClickCount() > 1) {
            try {
                Revendedor revendedor = new RevendedorBO().retrieve((Long) revendedoresJTable.getValueAt(revendedoresJTable.getSelectedRow(), 0));
                id_revendedor = revendedor.getId();
                txtNome.setText(revendedor.getNome());
                txtCPF.setText(revendedor.getCpf());
                txtDataNascimento.setText(new DateUtil().dateParaString(revendedor.getNascimento()));
                txtTelefone.setText(revendedor.getTelefone());
                txtCelular.setText(revendedor.getCelular());
                txtEmail.setText(revendedor.getEmail());
                txaEndereco.setText(revendedor.getEndereco());
                txaObservacoes.setText(revendedor.getObservacoes());
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_revendedoresJTableMouseClicked

