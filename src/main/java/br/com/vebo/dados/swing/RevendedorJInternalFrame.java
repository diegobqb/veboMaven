    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (validaForm()) {

            try {
                Revendedor revendedor = new RevendedorBO().retrieve(id_revendedor);
                revendedor.setNome(txtNome.getText());
                revendedor.setCpf(txtCPF.getText());
                revendedor.setNascimento(new DateUtil().stringParaDate(txtDataNascimento.getText()));
                revendedor.setTelefone(txtTelefone.getText());
                revendedor.setCelular(txtCelular.getText());
                revendedor.setEmail(txtEmail.getText());
                revendedor.setEndereco(txaEndereco.getText());
                revendedor.setObservacoes(txaObservacoes.getText());
                HibernateUtil.beginTransaction();
                new RevendedorBO().save(revendedor);
                HibernateUtil.commitTransaction();
                JOptionPane.showMessageDialog(this, "Revendedor alterado com sucesso", "Mensagem",  JOptionPane.INFORMATION_MESSAGE);
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
            }

            limpaForm();
            atualizaTabela();
            
        }
}//GEN-LAST:event_btnAlterarActionPerformed

