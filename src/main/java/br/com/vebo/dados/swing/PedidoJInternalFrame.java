    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        if (validaForm()) {
            Pedido novoPedido = new Pedido();
            novoPedido.setCalculo((Calculo)cbxCalculo.getSelectedItem());
            novoPedido.setPerfume((Perfume)cbxPerfume.getSelectedItem());
            novoPedido.setRevendedor((Revendedor)cbxRevendedor.getSelectedItem());
            novoPedido.setEntrega(new DateUtil().stringParaDate(txtEntrega.getText()));
            novoPedido.setPedido(new DateUtil().stringParaDate(txtData.getText()));
            novoPedido.setObservacao(txaObservacoes.getText());
            novoPedido.setValor(new DoubleUtil().stringParaDouble(txtValor.getText()));
            try {
                HibernateUtil.beginTransaction();
                new PedidoBO().save(novoPedido);
                HibernateUtil.commitTransaction();
                JOptionPane.showMessageDialog(this, "Pedido inserido com sucesso", "Mensagem",  JOptionPane.INFORMATION_MESSAGE);
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
            }

            limpaForm();
            atualizaTabela();

        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnCalcularValorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularValorActionPerformed
        txtValor.setText(new DoubleUtil().doubleParaString(new PrecoBO().calcularPreco((Perfume)cbxPerfume.getSelectedItem(), (Calculo)cbxCalculo.getSelectedItem()).getValorFinal(), "#,##0.00"));
    }//GEN-LAST:event_btnCalcularValorActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (validaForm()) {
            try {
                Pedido pedido = new PedidoBO().retrieve(idPedido);
                pedido.setCalculo((Calculo)cbxCalculo.getSelectedItem());
                pedido.setPerfume((Perfume)cbxPerfume.getSelectedItem());
                pedido.setRevendedor((Revendedor)cbxRevendedor.getSelectedItem());
                pedido.setEntrega(new DateUtil().stringParaDate(txtEntrega.getText()));
                pedido.setPedido(new DateUtil().stringParaDate(txtData.getText()));
                pedido.setObservacao(txaObservacoes.getText());
                pedido.setValor(new DoubleUtil().stringParaDouble(txtValor.getText()));

                HibernateUtil.beginTransaction();
                new PedidoBO().save(pedido);
                HibernateUtil.commitTransaction();
                JOptionPane.showMessageDialog(this, "Pedido alterado com sucesso", "Mensagem",  JOptionPane.INFORMATION_MESSAGE);
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
            }

            limpaForm();
            atualizaTabela();

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void pedidosJTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pedidosJTableMouseClicked
        if (evt.getClickCount() > 1) {
            int linhaSelecionada = pedidosJTable.getSelectedRow();
            try {
                Pedido pedido = new PedidoBO().retrieve((Long) pedidosJTable.getValueAt(linhaSelecionada, 0));
                idPedido = pedido.getId();
                cbxRevendedor.setSelectedItem(pedido.getRevendedor());
                cbxPerfume.setSelectedItem(pedido.getPerfume());
                cbxCalculo.setSelectedItem(pedido.getCalculo());
                txtData.setText(new DateUtil().dateParaString(pedido.getPedido()));
                txtEntrega.setText(new DateUtil().dateParaString(pedido.getEntrega()));
                txtValor.setText(new DoubleUtil().doubleParaString(pedido.getValor()));
                txaObservacoes.setText(pedido.getObservacao());
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_pedidosJTableMouseClicked

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o pedido selecionado?","Confirmação", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            int linhaSelecionada = pedidosJTable.getSelectedRow();
            try {
                Pedido pedido = new PedidoBO().retrieve((Long) pedidosJTable.getValueAt(linhaSelecionada, 0));
                HibernateUtil.beginTransaction();
                new PedidoBO().delete(pedido);
                HibernateUtil.commitTransaction();
                JOptionPane.showMessageDialog(this, "Pedido excluído com sucesso", "Mensagem",  JOptionPane.INFORMATION_MESSAGE);
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
            }
            atualizaTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

