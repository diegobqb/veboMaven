    public void atualizaComboboxes() {
    cbxRevendedorFiltro.setModel(new DefaultComboBoxModel());
    cbxRevendedor.setModel(new DefaultComboBoxModel());
    cbxPerfume.setModel(new DefaultComboBoxModel());
    cbxCalculo.setModel(new DefaultComboBoxModel());
    try {
        for (Revendedor revendedor : new RevendedorBO().findAll()) {
            cbxRevendedorFiltro.addItem(revendedor);
            cbxRevendedor.addItem(revendedor);
        }

        for (Perfume perfume : new PerfumeBO().findAll()) {
            cbxPerfume.addItem(perfume);
        }

        for (Calculo calculo : new CalculoBO().findAll()) {
            cbxCalculo.addItem(calculo);
        }

        
    }
    catch(Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
    }
    cbxRevendedorFiltro.updateUI();
    cbxRevendedor.updateUI();
    cbxPerfume.updateUI();
    cbxCalculo.updateUI();
}

    private void atualizaTabela() {

        PedidoTableModel tableModel = (PedidoTableModel) pedidosJTable.getModel();
        try {
            tableModel.setPedidos((List) new PedidoBO().findAll());
        } catch (DataAccessException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
        }
        pedidosJTable.updateUI();
    }

    private void atualizaTabela(Revendedor revendedor) {

        PedidoTableModel tableModel = (PedidoTableModel) pedidosJTable.getModel();
        try {
            Pedido filtro = new Pedido();
            filtro.setRevendedor(revendedor);
            tableModel.setPedidos((List) new PedidoBO().findByFilter(filtro));
        } catch (DataAccessException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro",  JOptionPane.ERROR_MESSAGE);
        }
        pedidosJTable.updateUI();
    }

    private boolean validaForm() {
        boolean retorno = true;
        StringBuffer mensagensErro = new StringBuffer();

        if (txtValor.getText().isEmpty()) {
            mensagensErro.append("O campo Valor é requerido.\n");
            retorno = false;
        }
        if (txtData.getText().isEmpty()) {
            mensagensErro.append("O campo Data é requerido.\n");
            retorno = false;
        }
        if (!retorno) {
            JOptionPane.showMessageDialog(this, mensagensErro.toString(), "Validação",  JOptionPane.ERROR_MESSAGE);
        }
        return retorno;
    }

    public void limpaForm() {
        idPedido = 0l;
        cbxRevendedor.setSelectedIndex(0);
        cbxPerfume.setSelectedIndex(0);
        cbxCalculo.setSelectedIndex(0);
        txtValor.setText("");
        txtData.setText("");
        txtEntrega.setText("");
        txaObservacoes.setText("");

    }

